# Obtener datos del usuario

# Para obtener datos introducidos por el usuario se utiliza la función input('Tu mensaje'). Por defecto el dato introducido por el usaurio es de tipo String por lo que se pueden usar varios métodos de converción para convertirlos otros tipos de datos como intergrer o float.

# -------------------- Sintaxis --------------------
nombre = input('Ingrese su nombre: ')
print(nombre)

# Convertir valores introducidos por el usaurio a integrer (int)
edad = int(input('Ingrese su edad: '))
print(type(edad))